#include "ft_printf.h"

int	ft_digit(int i)
{
	int	y;

	y = 0;
	ft_putnbr(i);
	if (i == 0)
	{
		return (1);
	}
	if (i < 0)
	{
		i = i * -1;
		y++;
	}
	while (i != 0)
	{
		i = i / 10;
		y++;
	}
	return (y);
}
